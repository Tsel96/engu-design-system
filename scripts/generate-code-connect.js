#!/usr/bin/env node
/**
 * Fetches all icon components from the Engu Figma file and generates
 * one .figma.ts file per category in code-connect/icons/.
 *
 * Requires FIGMA_TOKEN env var (personal access token).
 */

const https = require("https");
const fs = require("fs");
const path = require("path");

const FILE_KEY = "92ZwLCANCyRKezlcuQLOBW";
const SPRITE_PATH = "assets/icons/engu-icons-sprite.svg";
const OUT_DIR = path.join(__dirname, "..", "code-connect", "icons");

const token = process.env.FIGMA_TOKEN;
if (!token) {
  console.error("Error: FIGMA_TOKEN env var is required.");
  process.exit(1);
}

function get(url) {
  return new Promise((resolve, reject) => {
    const req = https.get(url, { headers: { "X-Figma-Token": token } }, (res) => {
      let body = "";
      res.on("data", (chunk) => (body += chunk));
      res.on("end", () => {
        if (res.statusCode !== 200) {
          reject(new Error(`HTTP ${res.statusCode}: ${body.slice(0, 200)}`));
        } else {
          resolve(JSON.parse(body));
        }
      });
    });
    req.on("error", reject);
  });
}

function slugCategory(category) {
  return category
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");
}

function nodeIdToUrl(nodeId) {
  // Figma node IDs use ":" in the API but "-" in URLs
  return nodeId.replace(/:/g, "-");
}

async function main() {
  console.log(`Fetching components from file ${FILE_KEY}…`);
  const data = await get(`https://api.figma.com/v1/files/${FILE_KEY}/component_sets`);

  const components = data.meta?.component_sets ?? [];
  console.log(`Found ${components.length} total components.`);

  // Filter to Icons/{Category}/{icon-name} pattern
  const iconComponents = components.filter((c) => /^Icons\/[^/]+\/.+/.test(c.name));
  console.log(`Matched ${iconComponents.length} icon components.`);

  if (iconComponents.length === 0) {
    console.warn("No icon components matched. Check the Figma file key and component naming.");
    process.exit(1);
  }

  // Group by category
  const byCategory = {};
  for (const c of iconComponents) {
    const parts = c.name.split("/"); // ["Icons", "Category", "icon-name"]
    const category = parts[1];
    const iconName = parts[2];
    if (!byCategory[category]) byCategory[category] = [];
    byCategory[category].push({ iconName, nodeId: c.node_id, key: c.key });
  }

  // Write output files
  fs.mkdirSync(OUT_DIR, { recursive: true });

  let totalFiles = 0;
  let totalConnections = 0;

  for (const [category, icons] of Object.entries(byCategory)) {
    const slug = slugCategory(category);
    const outFile = path.join(OUT_DIR, `${slug}.figma.ts`);

    const lines = [
      `// Code Connect — Icons / ${category}`,
      `// Auto-generated by scripts/generate-code-connect.js — do not edit by hand`,
      ``,
      `import figma from "@figma/code-connect";`,
      ``,
    ];

    for (const { iconName, nodeId } of icons) {
      const urlNodeId = nodeIdToUrl(nodeId);
      const url = `https://www.figma.com/design/${FILE_KEY}/Engu-Design-System?node-id=${urlNodeId}`;
      const spriteId = `engu-${iconName}`;
      const svgSnippet = `<svg viewBox="0 0 24 24" fill="none"><use href="${SPRITE_PATH}#${spriteId}"/></svg>`;

      lines.push(
        `figma.connect(`,
        `  "${url}",`,
        `  {`,
        `    example: () => \`${svgSnippet}\`,`,
        `  }`,
        `);`,
        ``
      );
      totalConnections++;
    }

    fs.writeFileSync(outFile, lines.join("\n"), "utf8");
    console.log(`  ✓ ${slug}.figma.ts  (${icons.length} icons)`);
    totalFiles++;
  }

  console.log(`\nDone. ${totalFiles} files, ${totalConnections} connections → ${OUT_DIR}`);
}

main().catch((err) => {
  console.error(err.message);
  process.exit(1);
});
